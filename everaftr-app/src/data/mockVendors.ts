export function getReviewsByVendorId(vendorId: string): VendorReview[] {
  // Mock reviews - in production, this would fetch from database
  const reviewTemplates = [
    { rating: 5, comment: 'Absolutely amazing service! Highly recommend for your celebration.' },
    { rating: 5, comment: 'Professional, punctual, and exceeded our expectations!' },
    { rating: 4, comment: 'Great service overall, very satisfied with the results.' },
    { rating: 5, comment: 'Made our special day even more memorable. Thank you!' },
    { rating: 4, comment: 'Good value for money and quality service.' },
  ];

  const vendor = getVendorById(vendorId);
  if (!vendor) return [];

  const numReviews = Math.min(3, Math.floor(vendor.reviewCount * 0.1)); // Show ~10% of reviews, max 3
  const reviews: VendorReview[] = [];

  for (let i = 0; i < numReviews; i++) {
    const template = reviewTemplates[i % reviewTemplates.length];
    reviews.push({
      id: `review-${vendorId}-${i}`,
      vendorId: vendorId,
      reviewerName: `Couple ${i + 1}`,
      rating: template.rating,
      date: new Date(2025, Math.floor(Math.random() * 12), Math.floor(Math.random() * 28) + 1).toISOString().split('T')[0],
      comment: template.comment,
    });
  }

  return reviews;
}
