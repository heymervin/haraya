// Helper functions
export function getVendorById(id: string): Vendor | undefined {
  return mockVendors.find(vendor => vendor.id === id);
}

export function getVendorsByCategory(category: string): Vendor[] {
  return mockVendors.filter(vendor => vendor.category === category);
}

export function getVendorsByLocation(location: string): Vendor[] {
  return mockVendors.filter(vendor =>
    vendor.location.toLowerCase().includes(location.toLowerCase())
  );
}

export function getVendorsByPriceRange(minPrice: number, maxPrice: number): Vendor[] {
  return mockVendors.filter(vendor =>
    vendor.priceRange.min <= maxPrice && vendor.priceRange.max >= minPrice
  );
}

export function getVendorCategories(): string[] {
  const categories = new Set(mockVendors.map(vendor => vendor.category));
  return Array.from(categories).sort();
}

